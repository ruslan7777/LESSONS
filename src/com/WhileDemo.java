//Demonstration of using a while loop

class WhileDemo {
        public static void main(String args[]) {
            char ch;
            //withdraw the letters of the English alphabet using a while loop
            ch = 'a';
            while (ch<='z') {
                System.out.print(ch+ " ");
            ch++;}
        }
}

